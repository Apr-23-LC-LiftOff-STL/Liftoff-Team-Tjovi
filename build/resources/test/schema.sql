CREATE TABLE CUSTOMER (
  id INT PRIMARY KEY,
  city VARCHAR(255),
  create_dt TIMESTAMP,
  email VARCHAR(255),
  first_name VARCHAR(255),
  last_name VARCHAR(255),
  mobile_number VARCHAR(255),
  pwd VARCHAR(255),
  role VARCHAR(255),
  state VARCHAR(255),
  street_address VARCHAR(255),
  suite_number VARCHAR(255),
  zip_code VARCHAR(255)
);